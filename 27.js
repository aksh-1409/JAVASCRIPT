// array push pop

// array shift unshift


let fruits = ["apple","mango","banana"];
//mutable object of array means we can change the array
console.log(fruits);
console.log(fruits.push("grapes")); //at the end
console.log(fruits);
console.log(fruits.pop()); //at the end
console.log(fruits);
console.log("``````````````````");
console.log(fruits.unshift("grapes")); //at the start
console.log(fruits);
console.log(fruits.shift()); //at the start
console.log(fruits);




